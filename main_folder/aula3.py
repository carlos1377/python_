"""
Python dinânico e forte

Dinâmico que entende o tipo de dado que foi atribuido

str -> string -> texto

"""

# Aspas Simples
print('carlos')
print(1, 'carlos "eduardo"')

# Aspas Duplas
print("carlos")
print(2, "carlos 'eduardo'")

# Escape
print("carlos \"eduardo\"")

# r
print(r"carlos \"eduardo\"")
